int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 1)
		return (1);
	if (nb == 0 || nb == 3 || nb == 2)
		return (0);
	i = 2;
	if (nb > 0)
	{
		while (i * i != nb && i < nb / 2)
		{
			i++;
		}
		if (i == nb / 2 && nb != 4)
			return (0);
		else
			return (i);
	}
	else
		return (0);
}
